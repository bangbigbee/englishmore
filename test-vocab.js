const normalizedText = text.replace(/[\u200B-\u200D\uFEFF]/g, '').replace(/\r/g, '\n');
const parts = normalizedText.split(/(TOPIC|CHUDE|CHU_DE|CATEGORY|WORD|MEANING|PHONETIC|EXAMPLE_VI|EXAMPLE_VIETNAMESE|VI|NGHIA_VI|DICH_NGHIA|EXAMPLE|SYNONYMS|SYNONYM|ANTONYMS|ANTONYM|COLLOCATIONS|COLLOCATION|TOEIC_TRAP|TRAP|MNEMONIC_IMAGE|MNEMONICURL|ENGLISH_DEFINITION|DEFINITION|ENGLISHDEFINITION|PART_OF_SPEECH|POS)\s*:/gi);

const items = [];
let currentItem = {};
let invalidCount = 0;

const flush = () => {
    if (currentItem.word && currentItem.meaning) {
        items.push({...currentItem});
    } else if (Object.keys(currentItem).length > 0) {
        if (currentItem.word || currentItem.meaning) invalidCount++;
    }
    currentItem = {};
}

for (let i = 1; i < parts.length; i += 2) {
    const rawKey = parts[i];
    const key = rawKey.toUpperCase().replace(/[^A-Z0-9]+/g, '_').replace(/^_+|_+$/g, '');
    let val = parts[i + 1] || '';
    val = val.replace(/_+/g, '').trim(); // the file has _______________________ separating them
    if (!val) continue;

    if (key === 'WORD') {
      if (currentItem.word) flush()
      currentItem.word = val
    } else if (key === 'MEANING') {
      currentItem.meaning = val
    }
}
flush();
console.log('Parsed array length:', items.length);
console.log('Invalid count:', invalidCount);
